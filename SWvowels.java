package trainingday7OBJECTSwithCONDITIONS;

public class SWvowels {
	public static void main(String[] args) {
		char letter = 'a';
		switch(letter){
		case 'a':
			System.out.println("vowels");
			break;
		case 'b':
			System.out.println("consonent");
			break;
		case 'c':
			System.out.println("consonent");
			break;
		case 'e':
			System.out.println("vowels");
			break;
		default:
			System.out.println("None of the above");
	
		}
	}
}
